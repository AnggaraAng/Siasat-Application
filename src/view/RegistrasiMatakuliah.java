    void showtable() {
        try {
            
            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            koneksi.koneksi();
            Statement st = koneksi.con.createStatement();

            String query = "SELECT * FROM registermatakuliah";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                String kode_matakuliah = rs.getString("kode_matakuliah");
                String nama_matakuliah = rs.getString("nama_matakuliah");
                String id_dosen = rs.getString("id_dosen");
                String hari = rs.getString("hari");
                model.insertRow(model.getRowCount(), new Object[]{kode_matakuliah, nama_matakuliah, id_dosen, hari});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }

