    private void btnInsertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertMouseClicked
        String kode_matakuliah = inputanKode.getText();
        String nama_matakuliah = inputanNama.getText();
        String id_dosen = inputanDosen.getText();
        String hari = inputanHari.getText();
        if(kode_matakuliah.isEmpty() || nama_matakuliah.isEmpty() || id_dosen.isEmpty() || hari.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap isi semua field diatas !");
        }else{
            try {
                Statement st = koneksi.con.createStatement();
                String query = "INSERT INTO registermatakuliah (kode_matakuliah,nama_matakuliah,id_dosen,hari)"
                        + " VALUES ('" + kode_matakuliah + "','" + nama_matakuliah + "','" + id_dosen + "','" + hari + "')";
                if (st.executeUpdate(query) == 1) {
                    JOptionPane.showMessageDialog(null, "Sukses Tambah Data Baru");
                    showtable();
                    inputanKode.setText("");
                    inputanNama.setText("");
                    inputanDosen.setText("");
                    inputanHari.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Gagal Tambah Data Baru");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error : " + e);
            }
        }
    }//GEN-LAST:event_btnInsertMouseClicked

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            int row = tabel.getSelectedRow();
            String kode_matakuliah = tabel.getModel().getValueAt(row, 0).toString();
            String nama_matakuliah = tabel.getModel().getValueAt(row, 1).toString();
            String id_dosen = tabel.getModel().getValueAt(row, 2).toString();
            String hari = tabel.getModel().getValueAt(row, 3).toString();
            inputanKode.setText(kode_matakuliah);
            inputanNama.setText(nama_matakuliah);
            inputanDosen.setText(id_dosen);
            inputanHari.setText(hari);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        try {
            String kode_matakuliah = inputanKode.getText();
            Statement st = koneksi.con.createStatement();
            String query = "DELETE FROM registermatakuliah where kode_matakuliah='"+kode_matakuliah+"'";
            if (st.executeUpdate(query) == 1) {
                JOptionPane.showMessageDialog(null, "Sukses Hapus Data ");
                showtable();
                inputanKode.setText("");
                inputanNama.setText("");
                inputanDosen.setText("");
                inputanHari.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Hapus Data");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }      
    }//GEN-LAST:event_btnDeleteMouseClicked

